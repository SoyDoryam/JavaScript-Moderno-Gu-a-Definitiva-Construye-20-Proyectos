// .toUpperCase() te va a permitir cambiar un texto a mayúsculas
const producto = "Monitor 24 pulgadas ";
console.log(producto.toUpperCase()); // Convierte el texto a mayúsculas: "MONITOR 24 PULGADAS"

// .toLowerCase() convierte un texto a minúsculas
console.log(producto.toLowerCase()); // Convierte el texto a minúsculas: "monitor 24 pulgadas"

// .toString() convierte un número a una cadena de texto
const cantidad = 200;
console.log(cantidad); // Muestra el número: 200
console.log(cantidad.toString()); // Convierte el número a string: "200"
