const numero1 = 30;
const numero2 = 20;

let resultado;

// Suma
resultado = numero1 + numero2;
console.log(`${numero1} + ${numero2} = ${resultado}`);
// Resta
resultado = numero1 - numero2;
console.log(`${numero1} - ${numero2} = ${resultado}`);
// Multiplicacion
resultado = numero1 * numero2;
console.log(`${numero1} * ${numero2} = ${resultado}`);
// Division
resultado = numero1 / numero2;
console.log(`${numero1} / ${numero2} = ${resultado}`);
// Modulo
resultado = numero1 % numero2;
console.log(`${numero1} % ${numero2} = ${resultado}`);
